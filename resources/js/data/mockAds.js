// Helper function to get a future date for the auction ad
const getAuctionEndTime = () => {
    const date = new Date();
    date.setDate(date.getDate() + 2); // 2 days from now
    date.setHours(date.getHours() + 4); // 4 hours from now
    date.setMinutes(date.getMinutes() + 37); // 37 minutes from now
    return date.toISOString();
};

